def formatar_colunas(texto):
    """
    Formata uma lista de colunas no formato desejado.

    Args:
        texto: String contendo as colunas no formato "`tabela`.`coluna`" or somente o nome da tabela.

    Returns:
        String formatada no estilo "`tabela` (`coluna1`, `coluna2`, ...)`.
    """

    # Separar as linhas e remover espaços em branco
    linhas = texto.splitlines()
    linhas = [linha.strip() for linha in linhas]

    # Extrair os nomes das tabelas e colunas
    colunas = []
    for linha in linhas:
        # Tentar separar tabela e coluna
        try:
            tabela, coluna = linha.split('.')
        except ValueError:
            tabela = linha
            coluna = None
        colunas.append((tabela, coluna))

    # Agrupar as colunas por tabela
    tabela_atual = None
    resultado = []
    for tabela, coluna in colunas:
        if tabela != tabela_atual:
            if tabela_atual:
                # Filtrar elementos validos (somente strings)
                colunas_validas = [col for col in colunas_tabela if col is not None]
                if colunas_validas:
                    resultado.append(f"{tabela_atual} ({', '.join(colunas_validas)})")
            tabela_atual = tabela
            colunas_tabela = [coluna]
        else:
            colunas_tabela.append(coluna)
    if tabela_atual:
        # Filtrar elementos validos (para a última tabela)
        colunas_validas = [col for col in colunas_tabela if col is not None]
        if colunas_validas:
            resultado.append(f"{tabela_atual} ({', '.join(colunas_validas)})")

    return '\n'.join(resultado)

